package br.com.generation.exercicios;

public class Preguica extends Animal {
	
	private double horasSono; 
	private int numArvores;
	
	
	public double getHorasSono() {
		return horasSono;
	}
	public void setHorasSono(double horasSono) {
		this.horasSono = horasSono;
	}
	public int getNumArvores() {
		return numArvores;
	}
	public void setNumArvores(int numArvores) {
		this.numArvores = numArvores;
	}
	
	
	

}
