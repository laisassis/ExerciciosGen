package br.com.generation.exercicios;

public class Cavalo extends Animal {
	
	private int numCelas;
	private int numFerraduras; 
	private String nomeCavaleiro;
	
	
	public int getNumCelas() {
		return numCelas;
	}
	public void setNumCelas(int numCelas) {
		this.numCelas = numCelas;
	}
	public int getNumFerraduras() {
		return numFerraduras;
	}
	public void setNumFerraduras(int numFerraduras) {
		this.numFerraduras = numFerraduras;
	}
	public String getNomeCavaleiro() {
		return nomeCavaleiro;
	}
	public void setNomeCavaleiro(String nomeCavaleiro) {
		this.nomeCavaleiro = nomeCavaleiro;
	} 
	
	
	

}
